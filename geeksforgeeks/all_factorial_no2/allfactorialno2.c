#include<stdio.h>

int main()
{
	int i,t,j;
	long long unsigned int n,a,p;
	scanf("%d",&t);
	
	for(i=0;i<t;i++)
	{
		scanf("%llu",&n);
		j=1;
		a=1;
		p=0;
		while(p<=n)
		{
			a=a*j;
			printf("%llu",a);
			printf(" ");
			j++;
			p=a*(j);
			
			//printf("%llu  %llu\n",n,p);
		}
		printf("\n");
	}
return 0;
}
