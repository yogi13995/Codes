#include<stdio.h>
#include<math.h>

int main()
{
	int j,t,n1,n2,i,a,count;
	scanf("%d",&t);
	
	for(i=0;i<t;i++)
	{
		scanf("%d %d",&n1,&n2);
		j=ceil(sqrt(n1));
		count = 0;
		while((j*j)<=n2)
		{
			a=j*j;
			
			if(a<=n2)
			{
				count++;
				//printf("%d\n",a);
				
			}
			
			j++;
			
		}
		printf("%d\n",count);
	}
return 0;
}
			
